import type {
  EmbeddingProfile,
  FortuneValence,
  LocalizedSymbolEntry,
  SceneModifier,
  SymbolEntry,
} from "../contracts/symbol-encyclopedia";

const embeddingProfile: EmbeddingProfile = {
  chunkTypes: ["searchText", "sceneModifier", "safeReading", "metaphorHook"],
};

function modifier(
  reading: string,
  triggerTerms: string[],
  weight = 0.78,
  fortuneValence?: FortuneValence,
): SceneModifier {
  return { reading, triggerTerms, weight, ...(fortuneValence ? { fortuneValence } : {}) };
}

type SceneInput = {
  key: string;
  reading: string;
  triggerTerms: string[];
  weight?: number;
  fortuneValence?: FortuneValence;
};

type CompactLocaleInput = {
  label: string;
  aliases: string[];
  searchText: string;
  coreMeanings: [string, string, string, ...string[]];
  lightReadings: [string, string, ...string[]];
  shadowReadings: [string, string, ...string[]];
  scenes: [SceneInput, SceneInput, SceneInput, ...SceneInput[]];
  contextQuestions: [string, string, ...string[]];
  metaphorHooks: [string, string, ...string[]];
  cardTitleSeeds: [string, string, ...string[]];
  smallPrescription: string;
  safeReading: string;
  avoidExpressions?: [string, string, ...string[]];
};

type CompactSymbolInput = {
  id: string;
  category: SymbolEntry["category"];
  subcategory: string;
  facets: [string, string, string, ...string[]];
  symbolRole?: SymbolEntry["symbolRole"];
  safetyLevel?: SymbolEntry["safetyLevel"];
  universalMeanings: [string, string, string, ...string[]];
  relatedIds: string[];
  sourceBasis: [string, string, ...string[]];
  ko: CompactLocaleInput;
  en: CompactLocaleInput;
};

function locale(input: CompactLocaleInput): LocalizedSymbolEntry {
  return {
    label: input.label,
    aliases: input.aliases,
    searchText: input.searchText,
    coreMeanings: input.coreMeanings,
    lightReadings: input.lightReadings,
    shadowReadings: input.shadowReadings,
    sceneModifiers: Object.fromEntries(
      input.scenes.map((scene) => [
        scene.key,
        modifier(scene.reading, scene.triggerTerms, scene.weight, scene.fortuneValence),
      ]),
    ) as Record<string, SceneModifier>,
    contextQuestions: input.contextQuestions,
    metaphorHooks: input.metaphorHooks,
    cardTitleSeeds: input.cardTitleSeeds,
    smallPrescriptions: [input.smallPrescription],
    safeReading: input.safeReading,
    avoidExpressions: input.avoidExpressions ?? ["미래를 단정하기", "현실 사건을 예언처럼 말하기"],
  };
}

function compactSymbol(input: CompactSymbolInput): SymbolEntry {
  return {
    id: input.id,
    status: "active",
    editorialStatus: "approved",
    category: input.category,
    subcategory: input.subcategory,
    facets: input.facets,
    symbolRole: input.symbolRole ?? ["primary_candidate"],
    safetyLevel: input.safetyLevel ?? "safe",
    accessTier: "free",
    embeddingProfile,
    universalMeanings: input.universalMeanings,
    relatedIds: input.relatedIds,
    sourceBasis: input.sourceBasis,
    locales: {
      ko: locale(input.ko),
      en: locale(input.en),
    },
  };
}

