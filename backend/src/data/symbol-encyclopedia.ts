import type {
  EmbeddingProfile,
  FortuneValence,
  InterpretationLensMap,
  LocalizedSymbolEntry,
  RuntimeSymbolEntry,
  SceneModifier,
  SupportedLocale,
  SymbolEntry,
  SymbolFortune,
} from "../contracts/symbol-encyclopedia";

function modifier(
  reading: string,
  triggerTerms: string[],
  weight = 0.75,
  fortuneValence?: FortuneValence,
): SceneModifier {
  return { reading, triggerTerms, weight, ...(fortuneValence ? { fortuneValence } : {}) };
}

function localized(input: {
  label: string;
  aliases: string[];
  searchText: string;
  coreMeanings: string[];
  lightReadings: string[];
  shadowReadings: string[];
  sceneModifiers: Record<string, SceneModifier>;
  contextQuestions: string[];
  metaphorHooks: string[];
  cardTitleSeeds: string[];
  smallPrescriptions: string[];
  safeReading: string;
  avoidExpressions: string[];
  fortune?: SymbolFortune;
}): LocalizedSymbolEntry {
  return input;
}

const embeddingProfile: EmbeddingProfile = {
  chunkTypes: ["searchText", "sceneModifier", "safeReading", "metaphorHook"],
};

function lenses(input: {
  coreMeanings: string[];
  universal: string[];
  eastAsian: string[];
  western: string[];
  avoidClaims: string[];
}): InterpretationLensMap {
  return {
    universal: {
      sourceBasis: ["scene_function", "everyday_metaphor"],
      coreMeanings: input.coreMeanings,
      safeTransform: input.universal,
      avoidClaims: input.avoidClaims,
    },
    east_asian: {
      sourceBasis: ["east_asian_symbolism", "product_editorial_rule"],
      safeTransform: input.eastAsian,
      avoidClaims: input.avoidClaims,
    },
    western: {
      sourceBasis: ["western_psychological", "mythic_association", "product_editorial_rule"],
      safeTransform: input.western,
      avoidClaims: input.avoidClaims,
    },
  };
}

type ActiveSymbolInput = {
  id: string;
  category: SymbolEntry["category"];
  subcategory: string;
  facets: string[];
  symbolRole: SymbolEntry["symbolRole"];
  safetyLevel?: SymbolEntry["safetyLevel"];
  accessTier?: SymbolEntry["accessTier"];
  universalMeanings: string[];
  tensionAxis: string[];
  relatedIds: string[];
  sourceBasis: string[];
  lens: Parameters<typeof lenses>[0];
  ko: LocalizedSymbolEntry;
  en: LocalizedSymbolEntry;
};

function activeSymbol(input: ActiveSymbolInput): SymbolEntry {
  return {
    id: input.id,
    status: "active",
    editorialStatus: "approved",
    category: input.category,
    subcategory: input.subcategory,
    facets: input.facets,
    symbolRole: input.symbolRole,
    safetyLevel: input.safetyLevel ?? "safe",
    accessTier: input.accessTier ?? "free",
    interpretationLenses: lenses(input.lens),
    embeddingProfile,
    universalMeanings: input.universalMeanings,
    tensionAxis: input.tensionAxis,
    relatedIds: input.relatedIds,
    sourceBasis: input.sourceBasis,
    locales: {
      ko: input.ko,
      en: input.en,
    },
  };
}

