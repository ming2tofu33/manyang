export function getRuntimeSymbolEntry(id: string, locale: SupportedLocale): RuntimeSymbolEntry {
  const entry = symbolEntries.find((candidate) => candidate.id === id);

  if (!entry) {
    throw new Error(`Unknown symbol entry: ${id}`);
  }

  const localizedEntry = entry.locales[locale];

  return {
    id: entry.id,
    category: entry.category,
    subcategory: entry.subcategory,
    facets: entry.facets,
    symbolRole: entry.symbolRole,
    safetyLevel: entry.safetyLevel,
    accessTier: entry.accessTier,
    label: localizedEntry.label,
    aliases: localizedEntry.aliases,
    searchText: localizedEntry.searchText,
    relatedIds: entry.relatedIds,
    evidence: {
      coreMeanings: localizedEntry.coreMeanings,
      lightReadings: localizedEntry.lightReadings,
      shadowReadings: localizedEntry.shadowReadings,
      sceneModifiers: localizedEntry.sceneModifiers,
      metaphorHooks: localizedEntry.metaphorHooks,
      avoidExpressions: localizedEntry.avoidExpressions,
      ...(localizedEntry.fortune ? { fortune: localizedEntry.fortune } : {}),
    },
  };
}

export function getRuntimeSymbolEntries(locale: SupportedLocale): RuntimeSymbolEntry[] {
  return symbolEntries.map((entry) => getRuntimeSymbolEntry(entry.id, locale));
}
